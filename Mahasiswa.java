public class Mahasiswa {
        String Nama;
        String NPM;
        String Jurusan;
        String Fakultas;

        public Mahasiswa (String Nama, String NPM, String Jurusan, String Fakultas) {
            this.Nama = Nama;
            this.NPM = NPM;
            this.Jurusan = Jurusan;
            this.Fakultas = Fakultas;
        }
}